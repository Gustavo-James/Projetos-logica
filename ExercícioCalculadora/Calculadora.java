public class Calculadora {
    
    double numero1;
    double numero2;
    
    public double calcularSoma() {
        return numero1 + numero2;
    }
    
    public double calcularSubtracao() {
        return numero1 - numero2;
    }
    
    public double calcularDivisao() {
        return numero1 / numero2;
    }
    
    public double calcularMultiplicacao() {
        return numero1 * numero2;
    }
}